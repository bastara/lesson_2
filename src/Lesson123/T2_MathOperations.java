package Lesson123;

public class T2_MathOperations {
    public static void main(String[] args) {
        //Попрактиковаться с арифметическими операторами
        int a = 10;
        int b = 5;
        double c = 8;
        double d = 3;
        System.out.println("Операция \"+\". " + (a + b) + " это для целых. " + (c + d) + " это для вещественных чисел");
        System.out.println("Операция \"-\". " + (a - b) + " это для целых. " + (c - d) + " это для вещественных чисел");
        System.out.println("Операция \"*\". " + (a * b) + " это для целых. " + (c * d) + " это для вещественных чисел");
        System.out.println("Операция \"/\". " + (a / b) + " это для целых. " + (c / d) + " это для вещественных чисел");
    }
}
